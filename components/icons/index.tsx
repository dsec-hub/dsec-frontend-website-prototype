// Social icon component that selects the right icon based on platform name
export function SocialIcon({
  name,
  ...props
}: { name: string } & IconProps): ReactElement | null {
  switch (name) {
    case "Discord":
      return <DiscordIcon {...props} />;
    case "Instagram":
      return <InstagramIcon {...props} />;
    case "LinkedIn":
      return <LinkedInIcon {...props} />;
    case "GitHub":
      return <GitHubIcon {...props} />;
    case "Facebook":
      return <FacebookIcon {...props} />;
    default:
      return null;
  }
}
